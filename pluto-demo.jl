p, q = Atom.(["p", "q"])

# ╔═╡ 6c7f587e-7aa8-4bf2-adfb-22b315d071e7
# Manually compose a formula
φ = ¬(p ∧ q)

# ╔═╡ b762a0a4-dff0-4cca-8324-e74f19406a1f
# Instantiate a propositional interpretation
I = TruthDict(["p" => true, "q" => false])

# ╔═╡ feaff836-9899-41b3-914b-3b84e831ea37
# Check the formula on the interpretation
check(φ, I)

