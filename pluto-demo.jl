# ╔═╡ 34342f5e-59a1-4442-8d7d-0bb74b8f4f4d
# An atom is a wrapper for anything representing a fact
Atom("I am alive")

# ╔═╡ 48c4a3c2-2334-4f8b-8288-cfa3a8ef7fb9
